package OOPSDEMO3;

public class Collage {
String name ;
int id ;
String university;
String course;
public Collage(String name, int id, String university, String course) {
	this.name = name;
	this.id = id;
	this.university = university;
	this.course = course;
}
public String getName() {
	return name;
}
public int getId() {
	return id;
}
public String getUniversity() {
	return university;
}
public String getCourse() {
	return course;
}


}
